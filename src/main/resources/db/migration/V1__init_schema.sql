-- ===============================
-- 1. CATÁLOGO BASE
-- ===============================

CREATE TABLE IF NOT EXISTS category (
  id          BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  name        VARCHAR(191) NOT NULL,
  slug        VARCHAR(191) NOT NULL,
  description TEXT,
  created_at  TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
  updated_at  TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
  deleted_at  TIMESTAMP,
  CONSTRAINT uk_category_name UNIQUE (name),
  CONSTRAINT uk_category_slug UNIQUE (slug)
);

CREATE TABLE IF NOT EXISTS tag (
  id          BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  name        VARCHAR(191) NOT NULL,
  slug        VARCHAR(191) NOT NULL,
  created_at  TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
  updated_at  TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
  deleted_at  TIMESTAMP,
  CONSTRAINT uk_tag_name UNIQUE (name),
  CONSTRAINT uk_tag_slug UNIQUE (slug)
);

CREATE TABLE IF NOT EXISTS product (
  id               BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  category_id      BIGINT NOT NULL,
  name             VARCHAR(191) NOT NULL,
  slug             VARCHAR(191) NOT NULL,
  description      TEXT,
  tax_rate_percent NUMERIC(5,2) NOT NULL,
  is_active        BOOLEAN NOT NULL DEFAULT TRUE,
  -- Sprint 5: Flags de marketing (source of truth a nivel producto)
  is_featured      BOOLEAN NOT NULL DEFAULT FALSE,
  is_daily_menu    BOOLEAN NOT NULL DEFAULT FALSE,
  is_new           BOOLEAN NOT NULL DEFAULT FALSE,
  created_at       TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
  updated_at       TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
  deleted_at       TIMESTAMP,
  CONSTRAINT uk_product_slug UNIQUE (slug),
  CONSTRAINT fk_product_category FOREIGN KEY (category_id)
    REFERENCES category(id) ON UPDATE RESTRICT ON DELETE RESTRICT
);

CREATE INDEX IF NOT EXISTS idx_product_category ON product (category_id);
CREATE INDEX IF NOT EXISTS idx_product_active ON product (is_active, deleted_at);
CREATE INDEX IF NOT EXISTS idx_product_featured ON product (is_featured);
CREATE INDEX IF NOT EXISTS idx_product_daily_menu ON product (is_daily_menu);

CREATE TABLE IF NOT EXISTS product_tag (
  product_id BIGINT NOT NULL,
  tag_id     BIGINT NOT NULL,
  CONSTRAINT pk_product_tag PRIMARY KEY (product_id, tag_id),
  CONSTRAINT fk_pt_product FOREIGN KEY (product_id) REFERENCES product(id)
    ON UPDATE RESTRICT ON DELETE RESTRICT,
  CONSTRAINT fk_pt_tag FOREIGN KEY (tag_id) REFERENCES tag(id)
    ON UPDATE RESTRICT ON DELETE RESTRICT
);

-- ===============================
-- 2. VARIANTES Y PRECIOS
-- ===============================

CREATE TABLE IF NOT EXISTS product_variant (
  id             BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  product_id     BIGINT NOT NULL,
  sku            VARCHAR(191),
  name           VARCHAR(191) NOT NULL,
  is_active      BOOLEAN NOT NULL DEFAULT TRUE,
  -- Stock management
  manages_stock  BOOLEAN NOT NULL DEFAULT FALSE,
  stock_quantity INT NOT NULL DEFAULT 0,
  -- Flags heredados (para retrocompatibilidad)
  is_featured    BOOLEAN NOT NULL DEFAULT FALSE,
  is_daily_menu  BOOLEAN NOT NULL DEFAULT FALSE,
  is_new         BOOLEAN NOT NULL DEFAULT FALSE,
  created_at     TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
  updated_at     TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
  deleted_at     TIMESTAMP,
  CONSTRAINT uk_variant_sku UNIQUE (sku),
  CONSTRAINT fk_variant_product FOREIGN KEY (product_id)
    REFERENCES product(id) ON UPDATE RESTRICT ON DELETE RESTRICT,
  CONSTRAINT chk_variant_stock_quantity_non_negative CHECK (stock_quantity >= 0)
);

CREATE INDEX IF NOT EXISTS idx_variant_product ON product_variant (product_id);
CREATE INDEX IF NOT EXISTS idx_variant_sku ON product_variant (sku);
CREATE INDEX IF NOT EXISTS idx_variant_active ON product_variant (product_id, is_active, deleted_at);

CREATE TABLE IF NOT EXISTS price_history (
  id                 BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  product_variant_id BIGINT NOT NULL,
  price              NUMERIC(10,2) NOT NULL,
  currency           VARCHAR(3) NOT NULL,
  valid_from         TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
  valid_to           TIMESTAMP,
  created_at         TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
  updated_at         TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
  deleted_at         TIMESTAMP,
  CONSTRAINT fk_ph_variant FOREIGN KEY (product_variant_id)
    REFERENCES product_variant(id) ON UPDATE RESTRICT ON DELETE RESTRICT,
  CONSTRAINT chk_currency CHECK (currency IN ('UYU','USD'))
);

CREATE INDEX IF NOT EXISTS idx_ph_variant_from ON price_history (product_variant_id, valid_from);
CREATE INDEX IF NOT EXISTS idx_ph_variant_to ON price_history (product_variant_id, valid_to);
-- Nota: índice parcial (WHERE valid_to IS NULL) no soportado en H2, usar consulta filtrada

-- ===============================
-- 3. IMÁGENES DE PRODUCTO
-- ===============================

CREATE TABLE IF NOT EXISTS product_image (
  id         BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  product_id BIGINT NOT NULL,
  object_key VARCHAR(512) NOT NULL,
  is_main    BOOLEAN NOT NULL DEFAULT FALSE,
  sort_order INT NOT NULL DEFAULT 0,
  created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
  updated_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
  deleted_at TIMESTAMP,
  CONSTRAINT fk_product_image_product FOREIGN KEY (product_id)
    REFERENCES product(id) ON UPDATE RESTRICT ON DELETE RESTRICT,
  CONSTRAINT chk_product_image_sort_order_non_negative CHECK (sort_order >= 0),
  CONSTRAINT uk_product_image_product_key UNIQUE (product_id, object_key)
);

CREATE INDEX IF NOT EXISTS idx_product_image_product ON product_image (product_id);
CREATE INDEX IF NOT EXISTS idx_product_image_main ON product_image (product_id, is_main);
CREATE INDEX IF NOT EXISTS idx_product_image_sort ON product_image (product_id, sort_order);

-- ===============================
-- 4. MODIFIERS (Sprint 5)
-- ===============================

CREATE TABLE IF NOT EXISTS modifier_group (
  id                 BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  product_variant_id BIGINT NOT NULL,
  name               VARCHAR(191) NOT NULL,
  min_select         INT NOT NULL DEFAULT 0,
  max_select         INT NOT NULL DEFAULT 1,
  selection_mode     VARCHAR(20) NOT NULL DEFAULT 'SINGLE',
  required_total_qty INT,
  default_option_id  BIGINT,
  sort_order         INT NOT NULL DEFAULT 0,
  is_active          BOOLEAN NOT NULL DEFAULT TRUE,
  created_at         TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
  updated_at         TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
  deleted_at         TIMESTAMP,
  CONSTRAINT fk_modifier_group_variant FOREIGN KEY (product_variant_id)
    REFERENCES product_variant(id) ON UPDATE RESTRICT ON DELETE RESTRICT,
  CONSTRAINT chk_modifier_group_selection_mode CHECK (selection_mode IN ('SINGLE','MULTI','QTY')),
  CONSTRAINT chk_modifier_group_bounds CHECK (max_select >= 0 AND min_select >= 0)
);

CREATE INDEX IF NOT EXISTS idx_modifier_group_variant ON modifier_group (product_variant_id);
CREATE INDEX IF NOT EXISTS idx_modifier_group_active ON modifier_group (product_variant_id, is_active, sort_order);

CREATE TABLE IF NOT EXISTS modifier_option (
  id                        BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  group_id                  BIGINT NOT NULL,
  name                      VARCHAR(191) NOT NULL,
  sort_order                INT NOT NULL DEFAULT 0,
  is_active                 BOOLEAN NOT NULL DEFAULT TRUE,
  price_delta               NUMERIC(10,2),
  is_exclusive              BOOLEAN NOT NULL DEFAULT FALSE,
  linked_product_variant_id BIGINT,
  created_at                TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
  updated_at                TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
  deleted_at                TIMESTAMP,
  CONSTRAINT fk_modifier_option_group FOREIGN KEY (group_id)
    REFERENCES modifier_group(id) ON UPDATE RESTRICT ON DELETE RESTRICT,
  CONSTRAINT fk_modifier_option_variant FOREIGN KEY (linked_product_variant_id)
    REFERENCES product_variant(id) ON UPDATE RESTRICT ON DELETE RESTRICT
);

CREATE INDEX IF NOT EXISTS idx_modifier_option_group ON modifier_option (group_id);
CREATE INDEX IF NOT EXISTS idx_modifier_option_variant ON modifier_option (linked_product_variant_id);

-- FK circular: default_option_id -> modifier_option
ALTER TABLE modifier_group
  ADD CONSTRAINT fk_modifier_group_default_option
  FOREIGN KEY (default_option_id) REFERENCES modifier_option(id)
    ON UPDATE RESTRICT ON DELETE SET NULL;

