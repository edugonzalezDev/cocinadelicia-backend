CREATE INDEX IF NOT EXISTS idx_order_item_modifier_order ON order_item_modifier (order_item_id);
CREATE INDEX IF NOT EXISTS idx_order_item_modifier_option ON order_item_modifier (modifier_option_id);

-- ===============================
-- 8. HISTORIAL DE ESTADO DE PEDIDOS
-- ===============================

CREATE TABLE IF NOT EXISTS order_status_history (
  id          BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  order_id    BIGINT NOT NULL,
  from_status VARCHAR(30),
  to_status   VARCHAR(30) NOT NULL,
  changed_by  VARCHAR(255) NOT NULL,
  changed_at  TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
  reason      VARCHAR(500),
  CONSTRAINT fk_order_status_history_order FOREIGN KEY (order_id)
    REFERENCES customer_order(id) ON DELETE CASCADE,
  CONSTRAINT chk_status_history_from CHECK (
    from_status IS NULL OR from_status IN ('CREATED','CONFIRMED','PREPARING','READY','OUT_FOR_DELIVERY','DELIVERED','CANCELLED')
  ),
  CONSTRAINT chk_status_history_to CHECK (
    to_status IN ('CREATED','CONFIRMED','PREPARING','READY','OUT_FOR_DELIVERY','DELIVERED','CANCELLED')
  )
);

CREATE INDEX IF NOT EXISTS idx_order_status_history_order_id ON order_status_history (order_id);
CREATE INDEX IF NOT EXISTS idx_order_status_history_changed_at ON order_status_history (changed_at);

-- ===============================
-- 9. PAGOS
-- ===============================

CREATE TABLE IF NOT EXISTS payment (
  id             BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  order_id       BIGINT NOT NULL,
  method         VARCHAR(30) NOT NULL,
  status         VARCHAR(30) NOT NULL,
  amount         NUMERIC(10,2) NOT NULL,
  currency       VARCHAR(3) NOT NULL,
  provider_tx_id VARCHAR(191),
  note           VARCHAR(255),
  created_at     TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
  updated_at     TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
  deleted_at     TIMESTAMP,
  CONSTRAINT fk_payment_order FOREIGN KEY (order_id) REFERENCES customer_order(id)
    ON UPDATE RESTRICT ON DELETE RESTRICT,
  CONSTRAINT chk_payment_method CHECK (
    method IN ('CASH','BANK_TRANSFER','MERCADOPAGO','CREDIT_CARD','DEBIT_CARD')
  ),
  CONSTRAINT chk_payment_status CHECK (
    status IN ('PENDING','AUTHORIZED','PAID','FAILED','REFUNDED','CANCELLED')
  ),
  CONSTRAINT chk_payment_currency CHECK (currency IN ('UYU','USD'))
);

CREATE INDEX IF NOT EXISTS idx_payment_order ON payment (order_id);
CREATE INDEX IF NOT EXISTS idx_payment_status_created ON payment (status, created_at);
