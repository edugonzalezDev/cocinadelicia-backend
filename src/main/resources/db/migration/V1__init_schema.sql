  CONSTRAINT fk_order_item_modifier_option FOREIGN KEY (modifier_option_id)
    REFERENCES modifier_option(id) ON UPDATE RESTRICT ON DELETE RESTRICT,
  CONSTRAINT chk_order_item_modifier_quantity CHECK (quantity > 0)
